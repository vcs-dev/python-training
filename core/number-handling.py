n1 = 10
n2 = 24.5

#sum of integer and decimal
print(n1 + n2)

#subtraction of integer and decimal
print(n1 - n2)

#multiplication
print(n1 * n2)

#n1 to the power of 3
print(n1 ** 3)

#n1 divided by 3
print(n1 / 3)

#n1 divided by 3 ignoring the decimal part
print(n1 // 3)

#modulus of n1 divided by 3
print(n1 % 3)

#precedence of operators
print(30 * 2 ** 3 - 8 / 4) #The same as (30 * (2 ** 3)) - (8 / 4)